function getWeaknesses(weaknesses){
    
    let finalWeaknessesList = new Map()
    for(let i=0; i < types.size; i++){
        let toPush = 1;
        weaknesses.forEach(weakness => {
            toPush *= types.get(weakness)[i]
        });

        if(toPush > 1) finalWeaknessesList.set(Array.from(types.entries())[i][0], toPush)
    }
    return finalWeaknessesList
}

